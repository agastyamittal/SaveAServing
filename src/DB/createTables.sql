CREATE TABLE BUSINESS_INFO (
    B_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    B_NAME TEXT NOT NULL,
    B_ZIP_CODE TEXT NOT NULL,
    B_STREET_NAME TEXT NOT NULL,
    B_HOUSE_NUMBER TEXT NOT NULL,
    B_PHONE TEXT NOT NULL,
    B_EMAIL TEXT NOT NULL
);

CREATE TABLE MEALS (
    M_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    M_MEAL_NAME TEXT NOT NULL,
    B_ID BIGINT NOT NULL REFERENCES BUSINESS_INFO(B_ID)
);

CREATE TABLE ORDERS (
    O_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    B_ID BIGINT NOT NULL REFERENCES BUSINESS_INFO(B_ID),
    M_ID BIGINT NOT NULL REFERENCES MEALS(M_ID),
    O_ORDER_QUANTITY INT NOT NULL,
    O_ORDER_DATE_TIME TIMESTAMP NOT NULL
);

CREATE TABLE VOLUNTEER_INFO (
    V_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    V_NAME TEXT NOT NULL,
    V_ZIP_CODE TEXT NOT NULL,
    V_STREET_NAME TEXT NOT NULL,
    V_HOUSE_NUMBER TEXT NOT NULL,
    V_PHONE TEXT NOT NULL,
    V_EMAIL TEXT NOT NULL
);


CREATE TABLE DONATION_CENTER_INFO (
    D_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    D_NAME TEXT NOT NULL,
    D_ZIP_CODE TEXT NOT NULL,
    D_STREET_NAME TEXT NOT NULL,
    D_HOUSE_NUMBER TEXT NOT NULL,
    D_PHONE TEXT NOT NULL,
    D_EMAIL TEXT NOT NULL          
);

CREATE TABLE BUSINESS_EVENTS (
    BE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    B_ID BIGINT NOT NULL REFERENCES BUSINESS_INFO(B_ID),
    BE_NAME TEXT NOT NULL,
    BE_START_TIME TIMESTAMP NOT NULL,
    BE_END_TIME TIMESTAMP NOT NULL
    M_ID BIGINT NOT NULL REFERENCES MEALS(M_ID),
    BE_MEAL_QUANTITY INT NOT NULL,
);
CREATE TABLE NOTIFICATIONS (
    N_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BE_ID BIGINT NOT NULL REFERENCES BUSINESS_EVENTS(BE_ID),
    N_RECIPIENT_TYPE TEXT NOT NULL,
    N_RECIPIENT_ID BIGINT NOT NULL,
    N_ACK BOOLEAN NOT NULL,
    N_DATE_TIME TIMESTAMP NOT NULL
);

CREATE TABLE VOLUNTEER_NOTIFICATIONS (
    VN_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BE_ID BIGINT NOT NULL REFERENCES BUSINESS_EVENTS(BE_ID),
    VN_RECIPIENT_TYPE TEXT NOT NULL,
    VN_RECIPIENT_ID BIGINT NOT NULL,
    VN_ACK BOOLEAN NOT NULL,
    VN_DATE_TIME TIMESTAMP NOT NULL
    D_ID BIGINT NOT NULL REFERENCES DONATION_CENTER_INFO(D_ID),
    B_ID BIGINT NOT NULL REFERENCES BUSINESS_INFO(B_ID),
);

CREATE TABLE TEAM_EVENTS (
    TE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TE_TEAM_ID BIGINT NOT NULL,
    TE_EVENT_ID BIGINT NOT NULL
);

