CREATE TABLE NOTIFICATIONS (
    N_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BE_ID BIGINT NOT NULL REFERENCES BUSINESS_EVENTS(BE_ID),
    N_RECIPIENT_TYPE TEXT NOT NULL,
    N_RECIPIENT_ID BIGINT NOT NULL,
    N_ACK BOOLEAN NOT NULL,
    N_DATE_TIME TIMESTAMP NOT NULL,
);

CREATE TABLE BUSINESS_EVENTS (
    BE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    B_ID BIGINT NOT NULL REFERENCES BUSINESSINFO(B_ID),
    BE_NAME TEXT NOT NULL,
    BE_START_TIME TIMESTAMP NOT NULL,
    BE_END_TIME TIMESTAMP NOT NULL,
);

CREATE TABLE TEAM_EVENTS (
    TE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    TE_TEAM_ID BIGINT NOT NULL,
    TE_EVENT_ID BIGINT NOT NULL
);